CREATE TABLE contributors (
  UID INTEGER PRIMARY KEY,
  name TEXT
);

CREATE TABLE contributions (
    UID INTEGER,
    TRACK_ID INTEGER,
    DAY DATE
);

CREATE TABLE tracks (
    TRACK_ID INTEGER,
    ALBUM_ID INTEGER,
    ARTIST TEXT,
    NAME TEXT
);

CREATE TABLE albums (
    ALBUM_ID INTEGER PRIMARY KEY,
    NAME TEXT
);


CREATE TABLE history (
    DAY DATE,
    THEME TEXT
);

